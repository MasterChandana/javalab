package lab1;

public class areaof3circle {
    public static void main(String[] args) {
      double r1=3.2;
      double r2=8.4;
      double r3=2.6;
      double area1=Math.PI*r1*r1;
      double area2=Math.PI*r2*r2;
      double area3=Math.PI*r3*r3;
      System.out.println("area of first circle="+area1);
      System.out.println("area of second circle="+area2);
      System.out.println("area of third circle="+area3);
    }
    
}
